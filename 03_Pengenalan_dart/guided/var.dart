void main() {
  // Menggunakan var
  var name = "Alice"; // Tipe data String
  var age = 25; // Tipe data Integer

  print("Nama: $name, Usia: $age");

  // Type annotation
  String name1 = "Bob"; // Tipe data String
  int age1 = 30; // Tipe data Integer
  print("Nama: $name1, Usia: $age1");

  // Multiple variable
  String firstName, lastName; // Tipe data String
  firstName = "Charlie";
  lastName = "Brown";
  print("Nama Lengkap: $firstName $lastName");
}
